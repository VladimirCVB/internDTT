        // https://docs.phalcon.io/3.4/en/request
        $request = new Request();

        // Check whether the request was made with method GET ( $this->request->isGet() )
        if ($request->isGet()) {

            $query = $this
                ->modelsManager
                ->createQuery(
                    'SELECT * FROM users'
                    );

            $users = $query->execute();

            // Set status code
            $response->setStatusCode(200, 'OK');

            // Set the content of the response
